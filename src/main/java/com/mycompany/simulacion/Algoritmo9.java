    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
         int n = Integer.parseInt(one.getText());
        float a = Float.parseFloat(two.getText());
        lista = new float[n];
        for (int i = 0; i < n; i++) {
            lista[i] = Float.parseFloat(JOptionPane.showInputDialog(null, "Ingresa el numero"+(i+1)));
        }
        s.newSeed(lista);
        for (int i = 0; i < n; i++) {
            table.addRow(new Object[]{String.valueOf(s.getOrderNumbers()[i]),s.getFi()[i],s.getZi()[i],s.getS()[i],s.getAlpha()[i],s.getOmega()[i]});
        }
        if (s.getMaxNumber() > a) {
            conclusion.setText("No se Puede Rechazar la Hipotesis Nula");
        }else{
            conclusion.setText("No se puede rechazar la Hipotesis Alternativa");
        }   
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Son Las 12 de la noche y sigues insertando numeros nulos?");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

